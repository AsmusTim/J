public class Solution {
    public static void main(String[] args) {
        sQuadrate();
    }
    public static void sQuadrate(){
        int i = 0;
        while(i < 10){
            int j = 0;
            while (j < 10){
                System.out.print("s");
                j++;
            }
            System.out.println();
            i++;
        }
    }
}
